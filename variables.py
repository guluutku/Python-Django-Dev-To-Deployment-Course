
x = 1  # integer
y = 2.5 # float
name = 'Gün' # string
is_cool = True # bool

# Multiple Assignments
x, y, name, is_cool = (1, 2.5, 'Gün', True)

print('x = ', x)
print('y = ', y)
print('name = ', name)
print('is_cool = ', is_cool)
print(x, y, name, is_cool)

print('-------------------')

# Math
a = x + y
print('a = ', a)

print('-------------------')

# Casting
# Changes variable's data type
b = str(x) # change x's type to string
c = int(y) # change y' type to int

print('-------------------')

# Check Type
print('Type a = ', type(a))
print('Type name = ', type(name))
print('Type x = ', type(x))
print('Type y = ', type(y))

print('Type b = ', type(b))
print('Type c = ', type(c))
